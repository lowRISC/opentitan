/* Verify that we can correctly hash the public key. */

.section .text.start

main:
  la x31, _stack
  bn.xor w31, w31, w31

  la x2, _params
  bn.lid x0, 0(x2)
  bn.wsrw MOD, w0

  la x2, _hash_pk_pk
  la x3, _hash_pk_tr
  jal x1, hash_pk

  ecall

.data
.balign 32

_hash_pk_pk:
.zero 2592
_hash_pk_tr:
.zero 64

_params:
.word 0x007fe001 /* q */
.word 0xfc7fdfff /* mu */
.word 0x0000a3fa /* n^-1 * R^3 mod q */
.word 0x00000000
.word 0x00000000
.word 0x00000000
.word 0x00000000
.word 0x00000000

_stack:
.zero 256
