/* Verify that the we can correctly compute `power2round`. */

.section .text.start

main:
  la x31, _stack
  bn.xor w31, w31, w31

  la x2, _params
  bn.lid x0, 0(x2)
  bn.wsrw MOD, w0

  la x2, _power2round_t
  la x3, _power2round_t0
  la x4, _power2round_t1
  jal x1, power2round

  ecall

.data
.balign 32

_power2round_t:
.zero 1024
_power2round_t0:
.zero 1024
_power2round_t1:
.zero 1024

_params:
.word 0x007fe001 /* q */
.word 0xfc7fdfff /* mu */
.word 0x0000a3fa /* n^-1 * R^3 mod q */
.word 0x00000000
.word 0x00000000
.word 0x00000000
.word 0x00000000
.word 0x00000000

_stack:
.zero 256
