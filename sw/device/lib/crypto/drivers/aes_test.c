static const aes_block_t kIv = {
    .data =
        {
            // Init Counter: f0f1f2f3f4f5f6f7f8f9fafbfcfdfeff
            0xf3f2f1f0,
            0xf7f6f5f4,
            0xfbfaf9f8,
            0xfffefdfc,
        },
};

// Expected counter after 4 blocks.
static const aes_block_t kFinalIv = {
    .data =
        {
            // kIv + 4 = f0f1f2f3f4f5f6f7f8f9fafbfcfdff03
            0xf3f2f1f0,
            0xf7f6f5f4,
            0xfbfaf9f8,
            0x03fffdfc,
        },
};

static const aes_block_t kPlaintext[] = {
    // Block#1: 6bc1bee22e409f96e93d7e117393172a
    {.data = 0xe2bec16b, 0x969f402e, 0x117e3de9, 0x2a179373},
    // Block#2: ae2d8a571e03ac9c9eb76fac45af8e51
    {.data = 0x578a2dae, 0x9cac031e, 0xac6fb79e, 0x518eaf45},
    // Block#3: 30c81c46a35ce411e5fbc1191a0a52ef
    {.data = 0x461cc830, 0x11e45ca3, 0x19c1fbe5, 0xef520a1a},
    // Block#4: f69f2445df4f9b17ad2b417be66c3710
    {.data = 0x45249ff6, 0x179b4fdf, 0x7b412bad, 0x10376ce6},
};

static const aes_block_t kCiphertext[] = {
    // Block#1: 874d6191b620e3261bef6864990db6ce
    {.data = 0x91614d87, 0x26e320b6, 0x6468ef1b, 0xceb60d99},
    // Block#2: 9806f66b7970fdff8617187bb9fffdff
    {.data = 0x6bf60698, 0xfffd7079, 0x7b181786, 0xfffdffb9},
    // Block#3: 5ae4df3edbd5d35e5b4f09020db03eab
    {.data = 0x3edfe45a, 0x5ed3d5db, 0x02094f5b, 0xab3eb00d},
    // Block#4: 1e031dda2fbe03d1792170a0f3009cee
    {.data = 0xda1d031e, 0xd103be2f, 0xa0702179, 0xee9c00f3},
};

// This is a weak share intended to exercise correct configuration of the
// hardware; in general, the key should be generated by either generating
// two shares and setting key = a ^ b, or generating a mask and setting
// a = key ^ mask, b = mask.
static const uint32_t share0[8] = {
    // NIST 800-38a F.5.1 CTR-AES128.Encrypt test vectors.
    // Key: 2b7e151628aed2a6abf7158809cf4f3c
    ~0x16157e2bU, ~0xa6d2ae28U, ~0x8815f7abU, ~0x3c4fcf09U, 0, 0, 0, 0};
static const uint32_t share1[8] = {
    UINT32_MAX, UINT32_MAX, UINT32_MAX, UINT32_MAX, 0, 0, 0, 0};

static status_t run_aes_test(void) {
  LOG_INFO("Configuring the AES hardware.");
  aes_key_t key = {
      .mode = kAesCipherModeCtr,
      .sideload = kHardenedBoolFalse,
      .key_len = 4,
      .key_shares = {share0, share1},
  };
  // Create the checksum of the key and store it in the key structure.
  key.checksum = aes_key_integrity_checksum(&key);

  TRY(aes_encrypt_begin(key, &kIv));

  aes_block_t ciphertext[ARRAYSIZE(kCiphertext)] = {0};
  aes_block_t *out = NULL;
  for (size_t i = 0; i < ARRAYSIZE(kPlaintext); ++i) {
    LOG_INFO("Processing block %d.", i);
    TRY(aes_update(out, &kPlaintext[i]));
    out = &ciphertext[i];
  }
  TRY(aes_update(out, NULL));

  CHECK_ARRAYS_EQ((uint32_t *)ciphertext, (uint32_t *)kCiphertext,
                  sizeof(ciphertext) / (sizeof(uint32_t)));

  LOG_INFO("Cleaning up.");
  aes_block_t final_iv;
  TRY(aes_end(&final_iv));

  CHECK_ARRAYS_EQ(final_iv.data, kFinalIv.data, kAesBlockNumWords);

  return OTCRYPTO_OK;
}

static status_t run_negative_test(void) {
  LOG_INFO("Running negative tests.");

  aes_key_t base_key = {
      .mode = kAesCipherModeCtr,
      .sideload = kHardenedBoolFalse,
      .key_len = 4,
      .key_shares = {share0, share1},
  };

  // Test Invalid Cipher Mode
  aes_key_t bad_mode_key = base_key;
  bad_mode_key.mode = (aes_cipher_mode_t)0xBAD0BAD0;
  bad_mode_key.checksum = aes_key_integrity_checksum(&bad_mode_key);
  CHECK(aes_encrypt_begin(bad_mode_key, &kIv).value == OTCRYPTO_BAD_ARGS.value);

  // Test Invalid Sideload Boolean
  aes_key_t bad_sideload_key = base_key;
  bad_sideload_key.sideload = (hardened_bool_t)0x12345678;
  bad_sideload_key.checksum = aes_key_integrity_checksum(&bad_sideload_key);
  CHECK(aes_encrypt_begin(bad_sideload_key, &kIv).value ==
        OTCRYPTO_BAD_ARGS.value);

  // Test Invalid Bool
  aes_key_t ctrl_reg_key = base_key;
  ctrl_reg_key.checksum = aes_key_integrity_checksum(&ctrl_reg_key);
  CHECK(aes_verify_ctrl_reg(ctrl_reg_key, (hardened_bool_t)0x12345678).value ==
        OTCRYPTO_BAD_ARGS.value);

  // Test Invalid Key Length
  aes_key_t bad_key_len = base_key;
  bad_key_len.key_len = 99;  // Not 128, 192, or 256
  bad_key_len.checksum = aes_key_integrity_checksum(&bad_key_len);
  CHECK(aes_encrypt_begin(bad_key_len, &kIv).value == OTCRYPTO_BAD_ARGS.value);

  aes_key_t valid_key = {
      .mode = kAesCipherModeCtr,
      .sideload = kHardenedBoolFalse,
      .key_len = 4,
      .key_shares = {share0, share1},
  };

  // Attempt to read output without providing any input
  aes_key_t premature_output_key = base_key;
  premature_output_key.checksum =
      aes_key_integrity_checksum(&premature_output_key);
  TRY(aes_encrypt_begin(premature_output_key, &kIv));
  aes_block_t dummy_dest;
  CHECK(aes_update(&dummy_dest, NULL).value == OTCRYPTO_RECOV_ERR.value);
  aes_block_t final_iv;
  TRY(aes_end(&final_iv));

  // Shadow Register Fault Test
  aes_key_t shadow_reg_key = base_key;
  shadow_reg_key.checksum = aes_key_integrity_checksum(&shadow_reg_key);
  CHECK_STATUS_OK(
      ottf_alerts_expect_alert_start(kTopEarlgreyAlertIdAesRecovCtrlUpdateErr));
  uint32_t ctrl_addr =
      TOP_EARLGREY_AES_BASE_ADDR + AES_CTRL_SHADOWED_REG_OFFSET;
  abs_mmio_write32(ctrl_addr, 0xAAAAAAAA);  // First write
  abs_mmio_write32(ctrl_addr, 0xBBBBBBBB);  // Second write (mismatch)
  CHECK(aes_encrypt_begin(shadow_reg_key, &kIv).value ==
        OTCRYPTO_RECOV_ERR.value);
  CHECK_STATUS_OK(ottf_alerts_expect_alert_finish(
      kTopEarlgreyAlertIdAesRecovCtrlUpdateErr));

  // Bad Checksum Test
  aes_key_t bad_checksum_key = base_key;
  uint32_t real_checksum = aes_key_integrity_checksum(&bad_checksum_key);
  bad_checksum_key.checksum = real_checksum ^ 0xFFFFFFFF;
  hardened_bool_t result = aes_key_integrity_checksum_check(&bad_checksum_key);
  CHECK(result == kHardenedBoolFalse);

  return OTCRYPTO_OK;
}

OTTF_DEFINE_TEST_CONFIG();

bool test_main(void) {
  CHECK_STATUS_OK(entropy_complex_init(kHardenedBoolFalse));
  CHECK_STATUS_OK(run_aes_test());
  CHECK_STATUS_OK(run_negative_test());

  return true;
}
