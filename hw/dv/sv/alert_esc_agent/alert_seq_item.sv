// An item that represents transactions on an alert interface

class alert_seq_item extends alert_esc_seq_item;

  // There are two "high-level" transactions that happen on the alert interface:
  //
  //   - an alert that is raised by the alert sender and then acked by the alert receiver
  //
  //   - a ping sent by the alert receiver, which gets an alert in response from the alert sender.
  //     This is then acked by the alert receiver.
  typedef enum {
    AlertTxn,
    PingTxn
  } txn_type_e;

  // The transaction type that corresponds to this item.
  //
  // This is only used for the active side of the agent. The meaning of the item depends on whether
  // the agent is acting as an alert sender or receiver.
  rand txn_type_e m_txn_type;

  // A delay injected by alert_receiver_driver before it actually sends a ping request. This allows
  // the driver to be sent a stream of back-to-back items without locking up the interface.
  rand int unsigned m_ping_delay;

  // The number of cycles the driver should wait before acking an alert (when being an alert
  // receiver) or clearing an alert after it has been acked (when being an alert sender).
  rand int unsigned m_ack_delay;

  // The number of cycles a receiver should continue to assert ack after the sender has dropped
  // alert_p.
  //
  // This only has an effect when acting as an alert receiver.
  rand int unsigned m_ack_stable;

  // The number of cycles to wait before sending an alert.
  //
  // This only has an effect when acting as an alert sender.
  rand int unsigned m_alert_delay;

  // Generated by alert_monitor: this item shows the current stage of the alert handshake
  alert_handshake_e m_alert_handshake_sta;

  // Constrain delays in the generated item
  extern constraint delay_c;

  `uvm_object_utils_begin(alert_seq_item)
    `uvm_field_enum(txn_type_e, m_txn_type, UVM_DEFAULT)
    `uvm_field_int (m_ping_delay,      UVM_DEFAULT)
    `uvm_field_int (m_ack_delay,       UVM_DEFAULT)
    `uvm_field_int (m_ack_stable,      UVM_DEFAULT)
    `uvm_field_int (m_alert_delay,     UVM_DEFAULT)
    `uvm_field_enum(alert_handshake_e, m_alert_handshake_sta, UVM_DEFAULT)
  `uvm_object_utils_end

  extern function new (string name = "");

endclass

constraint alert_seq_item::delay_c {
  soft m_ack_delay <= 32;
  soft m_ack_stable  dist {1 :/ 5, [2:10] :/ 5};
  soft m_alert_delay dist {0 :/ 5, [1:10] :/ 5};
}

function alert_seq_item::new (string name = "");
  super.new(name);
endfunction
